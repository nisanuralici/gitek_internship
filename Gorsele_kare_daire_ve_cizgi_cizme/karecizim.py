import cv2
img = cv2.imread("img.jpg",1)
cv2.namedWindow("original image", cv2.WINDOW_NORMAL)
cv2.imshow("original image",img)
cv2.waitKey(0)
cv2.rectangle(img, (100,100), (500,500), (255,25,25), 5)
cv2.namedWindow("rect image", cv2.WINDOW_NORMAL)
cv2.imshow("rect image",img)
cv2.waitKey(0)
img = cv2.imread("img.jpg",1)
cv2.rectangle(img,(100,100), (500,500), (255,25,25), -1)
cv2.namedWindow("rect image", cv2.WINDOW_NORMAL)
cv2.imshow("rect image",img)
cv2.waitKey(0)
cv2.destroyAllWindows()
